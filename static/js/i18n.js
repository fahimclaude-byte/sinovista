// Language management
const i18n = {
    currentLang: localStorage.getItem('sinovista_lang') || 'en',

    init() {
        this.applyLanguage(this.currentLang);
    },

    setLang(lang) {
        if (translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('sinovista_lang', lang);
            this.applyLanguage(lang);
            document.documentElement.lang = lang;
        }
    },

    applyLanguage(lang) {
        const t = translations[lang] || translations.en;
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (t[key]) {
                el.textContent = t[key];
            }
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (t[key]) {
                el.placeholder = t[key];
            }
        });
        // Update active language button
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === lang);
        });
    },

    t(key) {
        const t = translations[this.currentLang] || translations.en;
        return t[key] || key;
    }
};

// Theme management
const themeManager = {
    currentTheme: localStorage.getItem('sinovista_theme') || 'light',

    init() {
        this.applyTheme(this.currentTheme);
    },

    toggle() {
        const newTheme = this.currentTheme === 'light' ? 'dark' : 'light';
        this.setTheme(newTheme);
    },

    setTheme(theme) {
        this.currentTheme = theme;
        localStorage.setItem('sinovista_theme', theme);
        this.applyTheme(theme);
    },

    applyTheme(theme) {
        document.documentElement.setAttribute('data-theme', theme);
        const toggleBtn = document.getElementById('themeToggle');
        if (toggleBtn) {
            toggleBtn.innerHTML = theme === 'dark' ? '☀️' : '🌙';
        }
    }
};

// Initialize on load
if (typeof window !== 'undefined') {
    // Apply theme immediately (before render) to avoid flash
    const savedTheme = localStorage.getItem('sinovista_theme') || 'light';
    document.documentElement.setAttribute('data-theme', savedTheme);

    document.addEventListener('DOMContentLoaded', () => {
        i18n.init();
        themeManager.init();
    });
}
